package com.example.notes2;

public class ExifHolder {

	String tag,name;
	
 public ExifHolder (String t, String n){
	 this.tag=t;
	 this.name=n;
 }

public String getTag() {
	return tag;
}

public void setTag(String tag) {
	this.tag = tag;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}
	
	
	
	
}
